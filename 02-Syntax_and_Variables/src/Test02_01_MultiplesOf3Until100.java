class Test02_01_MultiplesOf3Until100 {
	public static void main(String[] args) {
		
		/*
		 * To Obtain all the multiples of 3
		 * I just start counting from 3 and start adding up 3 until the result is right bellow 100
		 */
		
		for (int i = 3; i < 100; i += 3) {
			System.out.println(i);
		}
	}
}