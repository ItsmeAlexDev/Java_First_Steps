public class Test02_00_TestingFor {

	public static void main(String[] args) {
		
		/*
		 * Counting from 0 to 10
		 * 
		 * the initial value of "i" is the starting number of the count
		 * the value of "x" in "i <= x" is the ending number of the count
		 * the last parameter is how the values should increase
		 */
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}

	}
}